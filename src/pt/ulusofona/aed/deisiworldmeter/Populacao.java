package pt.ulusofona.aed.deisiworldmeter;

public class Populacao {
    int id;
    int ano;
    int masculino;
    int feminino;
    float densidade;

    public Populacao(int id, int ano, int masculino, int feminino, float densidade) {
        this.id = id;
        this.ano = ano;
        this.masculino = masculino;
        this.feminino = feminino;
        this.densidade = densidade;
    }


}
